#include <bits/stdc++.h>
using namespace std;
using ll = long long;

const ll MOD = 1e9 + 7;
const ll INF = 1e18;

ll gcd(ll a, ll b) { return b ? gcd(b, a % b) : a; }
ll lcm(ll a, ll b) { return a / gcd(a, b) * b; }

const string YES = "Yes";
const string NO = "No";

void solve(long long M, long long H) {
  if (H % M == 0) {
    cout << YES << endl;
  } else {
    cout << NO << endl;
  }
}

int main() {
  // cout << fixed << setprecision(5);

  long long M;
  scanf("%lld", &M);
  long long H;
  scanf("%lld", &H);
  solve(M, H);
  return 0;
}
